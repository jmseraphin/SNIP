export function t(key) {
  const lang = getLang();
  return translations[lang]?.[key] || translations.fr[key] || key;
}

export function tr(key, values = {}) {
  return Object.entries(values).reduce(
    (result, [name, value]) => result.replace(`{{${name}}}`, value),
    t(key)
  );
}