import { useEffect, useState } from "react";

export function getLang() {
  return localStorage.getItem("snip_lang") || "fr";
}

export function setLang(lang) {
  localStorage.setItem("snip_lang", lang);
  document.documentElement.setAttribute("lang", lang);
  window.dispatchEvent(new Event("snip-language-change"));
  window.dispatchEvent(new Event("snip-preferences-changed"));
}

export function useLang() {
  const [lang, setCurrentLang] = useState(getLang());

  useEffect(() => {
    const handleLanguageChange = () => {
      setCurrentLang(getLang());
    };

    window.addEventListener("snip-language-change", handleLanguageChange);
    window.addEventListener("snip-preferences-changed", handleLanguageChange);

    return () => {
      window.removeEventListener("snip-language-change", handleLanguageChange);
      window.removeEventListener("snip-preferences-changed", handleLanguageChange);
    };
  }, []);

  return lang;
}

